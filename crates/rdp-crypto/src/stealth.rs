#[cfg(test)]
mod tests {
    use super::*;
    use rand::rngs::OsRng;

    #[test]
    fn test_stealth_address_roundtrip() {
        let mut rng = OsRng;
        
        // Receiver generates keypair
        let receiver_keys = StealthKeyPair::generate(&mut rng);
        let meta_address = receiver_keys.meta_address();
        
        // Sender generates stealth address
        let stealth_output = generate_stealth_address(&meta_address, &mut rng).unwrap();
        
        // Receiver scans and finds the stealth address
        let is_mine = check_stealth_address(
            &receiver_keys.view_secret,
            &receiver_keys.spend_pubkey,
            &stealth_output.ephemeral_pubkey,
            &stealth_output.stealth_pubkey,
        ).unwrap();
        
        assert!(is_mine, "Receiver should detect their stealth address");
    }

    #[test]
    fn test_stealth_private_key_derivation() {
        let mut rng = OsRng;
        
        // Receiver generates keypair
        let receiver_keys = StealthKeyPair::generate(&mut rng);
        let meta_address = receiver_keys.meta_address();
        
        // Sender generates stealth address
        let stealth_output = generate_stealth_address(&meta_address, &mut rng).unwrap();
        
        // Receiver derives private key
        let stealth_privkey = derive_stealth_private_key(
            &receiver_keys.spend_secret,
            &receiver_keys.view_secret,
            &stealth_output.ephemeral_pubkey,
        ).unwrap();
        
        // Verify: stealth_privkey * G should equal stealth_pubkey
        let derived_scalar = Scalar::from_bytes_mod_order(&stealth_privkey);
        let derived_pubkey = Point::basepoint().mul(&derived_scalar);
        
        assert_eq!(
            derived_pubkey.to_bytes(),
            stealth_output.stealth_pubkey,
            "Derived private key should correspond to stealth public key"
        );
    }

    #[test]
    fn test_different_receivers_get_different_addresses() {
        let mut rng = OsRng;
        
        // Two different receivers
        let receiver1 = StealthKeyPair::generate(&mut rng);
        let receiver2 = StealthKeyPair::generate(&mut rng);
        
        // Generate stealth addresses for each
        let stealth1 = generate_stealth_address(&receiver1.meta_address(), &mut rng).unwrap();
        let stealth2 = generate_stealth_address(&receiver2.meta_address(), &mut rng).unwrap();
        
        // Stealth addresses should be different
        assert_ne!(
            stealth1.stealth_pubkey,
            stealth2.stealth_pubkey,
            "Different receivers should get different stealth addresses"
        );
    }

    #[test]
    fn test_same_receiver_gets_different_addresses() {
        let mut rng = OsRng;
        
        // Same receiver
        let receiver = StealthKeyPair::generate(&mut rng);
        
        // Generate two stealth addresses for same receiver
        let stealth1 = generate_stealth_address(&receiver.meta_address(), &mut rng).unwrap();
        let stealth2 = generate_stealth_address(&receiver.meta_address(), &mut rng).unwrap();
        
        // Each stealth address should be unique (different ephemeral keys)
        assert_ne!(
            stealth1.stealth_pubkey,
            stealth2.stealth_pubkey,
            "Same receiver should get different stealth addresses each time"
        );
        
        // But receiver should be able to detect both
        assert!(check_stealth_address(
            &receiver.view_secret,
            &receiver.spend_pubkey,
            &stealth1.ephemeral_pubkey,
            &stealth1.stealth_pubkey,
        ).unwrap());
        
        assert!(check_stealth_address(
            &receiver.view_secret,
            &receiver.spend_pubkey,
            &stealth2.ephemeral_pubkey,
            &stealth2.stealth_pubkey,
        ).unwrap());
    }

    #[test]
    fn test_wrong_receiver_cannot_detect() {
        let mut rng = OsRng;
        
        // Intended receiver
        let receiver = StealthKeyPair::generate(&mut rng);
        
        // Wrong receiver (attacker)
        let attacker = StealthKeyPair::generate(&mut rng);
        
        // Generate stealth address for intended receiver
        let stealth_output = generate_stealth_address(&receiver.meta_address(), &mut rng).unwrap();
        
        // Attacker tries to scan
        let attacker_check = check_stealth_address(
            &attacker.view_secret,
            &attacker.spend_pubkey,
            &stealth_output.ephemeral_pubkey,
            &stealth_output.stealth_pubkey,
        ).unwrap();
        
        assert!(!attacker_check, "Wrong receiver should not detect stealth address");
    }

    #[test]
    fn test_meta_address_serialization() {
        let mut rng = OsRng;
        let keypair = StealthKeyPair::generate(&mut rng);
        let meta = keypair.meta_address();
        
        let bytes = meta.to_bytes();
        let restored = StealthMetaAddress::from_bytes(&bytes);
        
        assert_eq!(meta, restored, "Meta address should survive serialization roundtrip");
    }
}
